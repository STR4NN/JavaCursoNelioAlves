package org.example.Lambdas.InterfacePratice;

public class Product extends Object {
    // Atributos constantes.
    final String nome;
    final double preco;
    final double desconto;

    public Product(String nome, double preco, double disconto) {

        this.nome = nome;
        this.preco = preco;
        this.desconto = disconto;
    }
}
